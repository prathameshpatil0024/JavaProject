package com.company.literal;

public class Literals {
    public static void main(String[] args) {

        int a = 70;    //decimal
        int b = 070;   //octal
        int c = 0X90f;  //  hexa decimal
        int d = 0b11;   //binary
        float e = 12.25f;
        double h=1206.98;
        char f = 34;
        String s = "hello java";
        boolean g = true;

        System.out.println(a);
        System.out.println(b);
        System.out.println(c);
        System.out.println(d);
        System.out.println(e);
        System.out.println(f);
        System.out.println(s);
        System.out.println(g);
        System.out.println(h);
        System.out.println("this is software\n testing class");
        System.out.println("this is \"software testing\" class");

    }

}



